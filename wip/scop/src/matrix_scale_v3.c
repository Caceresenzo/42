#include "matrix.h"

t_m4f*
m4f_scale_v3(t_m4f *m4, const t_v3f *v3)
{
	float x = v3->x;
	float y = v3->y;
	float z = v3->z;

	m4->m[0][0] *= x;
	m4->m[0][1] *= x;
	m4->m[0][2] *= x;
	m4->m[0][3] *= x;

	m4->m[1][0] *= y;
	m4->m[1][1] *= y;
	m4->m[1][2] *= y;
	m4->m[1][3] *= y;

	m4->m[2][0] *= z;
	m4->m[2][1] *= z;
	m4->m[2][2] *= z;
	m4->m[2][3] *= z;

	return (m4);
}
