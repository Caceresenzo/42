#include "vector.h"

t_v3f*
v3f_sub_v3(t_v3f *v3, const t_v3f *a, const t_v3f *b)
{
	v3->x = a->x - b->x;
	v3->y = a->y - b->y;
	v3->z = a->z - b->z;

	return (v3);
}
