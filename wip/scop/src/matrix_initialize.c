#include "matrix.h"

void
m4f_initialize(t_m4f *m4, float value)
{
	m4->m[0][0] = value;
	m4->m[0][1] = 0.0f;
	m4->m[0][2] = 0.0f;
	m4->m[0][3] = 0.0f;
	m4->m[1][0] = 0.0f;
	m4->m[1][1] = value;
	m4->m[1][2] = 0.0f;
	m4->m[1][3] = 0.0f;
	m4->m[2][0] = 0.0f;
	m4->m[2][1] = 0.0f;
	m4->m[2][2] = value;
	m4->m[2][3] = 0.0f;
	m4->m[3][0] = 0.0f;
	m4->m[3][1] = 0.0f;
	m4->m[3][2] = 0.0f;
	m4->m[3][3] = value;
}
