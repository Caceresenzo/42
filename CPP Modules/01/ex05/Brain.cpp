#include <iostream>
#include <iomanip>
#include <sstream>
#include <string>

#include "Brain.hpp"

std::string
Brain::identify(void) const
{
	std::stringstream stream;

	stream << std::showbase << std::hex;
	stream << std::uppercase << (long)this;

	return (stream.str());
}
